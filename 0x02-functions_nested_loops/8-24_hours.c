#include "main.h"

/**
 * jack_bauer - print 24 hours
 *
 */

void jack_bauer(void)
{
	int i, j, k, z;

	for (i = '0'; i <= '2'; i++)
	{
	for (j = '0'; i <= '3'; i++)
	{
		for (k = '0'; i <= '5'; i++)
		{
			for (z = '0'; i <= '9'; i++)
			{
				_putchar(i);
				_putchar(j);
				_putchar(':');
				_putchar(k);
				_putchar(z);
				_putchar('\n');
			}
}
}
}
}
